    public void showAllSupplier(){
     DefaultTableModel model=new DefaultTableModel();
  
        tblsupplierreport.setModel(model);
        Vector row;
        model.addColumn("ID");
        model.addColumn("First Name");
        model.addColumn("LastName"); 
        model.addColumn(" address");
        
        model.addColumn("Contact");
       
        model.addColumn("email");
      
        try{
            
            List<Supplier> Allusers = new ArrayList<Supplier>();
            Supplier user=new Supplier();
          
            JCheckBox jb=new JCheckBox();
            Allusers=user.ViewAllSuppliers();
             for (Supplier u: Allusers) {
                row = new Vector();
                row.add(u.getId());
                row.add(u.getFirst_name());
                row.add(u.getLast_name());
                row.add(u.getAddress());
                row.add(u.getContact_no());
                row.add(u.getEmail());
              
               
                model.addRow(row);
             }            
        }        
        catch(Exception ex){
            System.out.println("Error : "+ex.getMessage());
        }
    }
